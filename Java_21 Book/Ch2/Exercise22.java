public class Exercise22 {
    public static void main(String[] arguments) {
        
        int first = 97;
        int second = 14;
        
        int divResult = first / second;
        int modResult = first % second;
        
        System.out.println(first + " divided by " + second + " equals: \t\t\t" + divResult);
        System.out.println("The remainder of " + first + " divided by " + second + " equals: \t" + modResult);
    }
}